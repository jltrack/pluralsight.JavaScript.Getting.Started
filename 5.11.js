// Objects and Symbols

//Objects are foundational
//In an Object, set properties inside an object. Properties are similar to variables
//Symbols are advanced

let person = {
    firstName: ‘John’,
    lastName: ‘Adams’
};
showMessage(typeof person);

//object is printed

let person = {
    firstName: ‘John’,
    lastName: ‘Adams’
};
showMessage(person.firstName);

//John is printed
//The dot operator takes an object and pulls up one of the properties
